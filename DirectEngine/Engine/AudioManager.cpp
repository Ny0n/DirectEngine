#include "pch.h"

bool AudioManager::isActive = true;
IXAudio2* AudioManager::pXAudio2 = nullptr;
IXAudio2MasteringVoice* AudioManager::pMasterVoice = nullptr;
HRESULT AudioManager::hr = 0;

void AudioManager::Initialize()
{
	// XAudio2 Interface

	HRSOUND(CoInitializeEx(nullptr, COINIT_MULTITHREADED));
	
	HRSOUND(XAudio2Create(&pXAudio2, 0, XAUDIO2_DEFAULT_PROCESSOR));
	
	HRSOUND(pXAudio2->CreateMasteringVoice(&pMasterVoice));

	SetActive(false);
}

void AudioManager::Clean()
{
	// if (pXAudio2 != nullptr)
	// 	pXAudio2->Release();
}

void AudioManager::SetActive(bool active)
{
	if (isActive == active)
		return;

	isActive = active;

	if (pXAudio2 != nullptr)
	{
		if (isActive)
			pXAudio2->StartEngine();
		else
			pXAudio2->StopEngine();
	}
}

bool AudioManager::IsActive()
{
	return isActive;
}

